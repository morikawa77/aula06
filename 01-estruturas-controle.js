// Condicionais:
// if: Executa um bloco de código se a condição for verdadeira.

if (x > 10) {
  console.log("X é maior que 10");
}

// else: Executa um bloco de código se a condição if não for verdadeira.

else {
  console.log("X não é maior que 10");
}

// Loops:
// for: Executa um bloco de código um número específico de vezes.

for (let i = 0; i < 5; i++) {
  console.log(i);
}

// while: Executa um bloco de código enquanto a condição for verdadeira.

while (x < 5) {
  x++;
}